#Programa para verificar o While
 
# Rotina incrementa x
 
x = 8
 
#inicio
#Cardinalidade desta extrutura é: (0,N) --> (0,10)
 
while(x < 10):
    print(f'X é igual a {x} 1')  #f quer dizer f-string, ou seja, toda variavel dentro de chaves será interpretada e concatenada como texto
    #x = x + 1
    x +=1
 
print(f'X é igual a {x} 2')